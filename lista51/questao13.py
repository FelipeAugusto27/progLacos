'''
Desenvolver um programa que imprima a tabuada de 3 a 6.
'''

# Processamento e Output
# Tabuada de 3
cont1 = 1
print("Tabuada de 3:")
while cont1 <= 10:
    print(f"3x{cont1}={3*cont1}")
    cont1 = cont1 + 1
# Tabuada de 6
cont2 = 1
print("Tabuada de 6:")
while cont2 <= 10:
    print(f"6x{cont2}={6*cont2}")
    cont2 = cont2 + 1
